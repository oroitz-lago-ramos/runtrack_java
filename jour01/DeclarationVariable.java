package jour01;

public class DeclarationVariable {
    
    public static void main(String[] args) {
        // Declaration of variables
        int a,b,c;
        // Assign 0 to all variables in a ligne
        a = b = c = 0;
        // Print the value of a,b,c
        System.out.println("a = " + a + ", b = " + b + ", c = " + c);
    }
}
